<template>
  <div v-if="auth?.type == 'credentials'">
    <template v-if="!successMessage">
      <slot name="head">
        <div class="reset-heading">
          <h2 class="reset-heading__title">
            {{ $t('account.reset.reset_pswd') }}
          </h2>
          <p class="reset-heading__description">
            {{ $t('account.reset.description') }}
          </p>
        </div>
      </slot>
      <form @submit.prevent="submit">
        <div class="reset-form">
          <div class="reset-form__row">
            <label class="" for="email">{{ $t('account.address.email') }}</label>
            <input
              id="email"
              v-model="login"
              class=""
              type="email"
              @keyup="checkValidity('login', $event)"
              required
              placeholder="Enter email address"
            />
          </div>
          <div class="reset-form__error" v-if="error.login">
            {{ error.login }}
          </div>
          <div class="w-full p-3">
            <div class="reset-btn">
              <div class="reset-btn__wrapper">
                <button type="submit" class="">
                  {{ $t('account.reset.reset_btn') }}
                </button>
              </div>
            </div>
          </div>
          <div class="w-full p-3">
            <slot name="footer">
              <div class="footer-error" v-if="error.auth">
                {{ error.auth }}
              </div>
            </slot>
          </div>
        </div>
      </form>
    </template>
    <div v-else class="reset-success">
      <slot name="success">
        <h2 class="reset-success__title">
          {{ $t('account.reset.reset_pswd') }}
        </h2>
        {{ $t('account.reset.reset_success') }}
        <div>
          <nuxt-link :to="localePath('account')" class="btn btn-primary">
            <icon name="left" />
            {{ $t('btn.back') }}
          </nuxt-link>
        </div>
      </slot>
    </div>
  </div>
</template>
<script lang="ts">
import type { AuthCredentialService } from '#services'
import LogoVue from '../global/Logo.vue'

export default defineNuxtComponent({
  name: 'AccountLogin',
  components: {
    logo: LogoVue
  },
  data() {
    const route = useRoute()

    return {
      login: (route?.query?.email as string) || ('' as string | null),
      successMessage: false as boolean,
      error: {
        auth: null as string | null,
        login: null as string | null
      }
    }
  },
  setup() {
    const localePath = useLocalePath()
    const auth = useShopinvaderService('auth')
    return {
      localePath,
      auth
    }
  },
  methods: {
    checkValidity(input: string, e: Event) {
      this.error.login = null
      const target = e.target as HTMLInputElement
      if (target?.validity?.valid == false) {
        if (input == 'login') {
          this.error.login = this.$t('error.login.email')
        }
      } else {
        this.error.login = null
      }
    },
    async submit(_e: Event) {
      const auth = useShopinvaderService('auth') as AuthCredentialService
      if (this?.login) {
        try {
          const resetPasswordSent = (await auth?.resetPassword(this.login)) as boolean
          if (resetPasswordSent) {
            this.successMessage = true
          }
          this.login = ''
        } catch (e: any) {
          console.error(e)
          this.error.auth = e?.message || this.$t('error.login.unable_to_login')
        }
      }
    }
  }
})
</script>
